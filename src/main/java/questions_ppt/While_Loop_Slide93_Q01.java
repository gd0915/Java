package questions_ppt;

public class While_Loop_Slide93_Q01 {

    public static void main(String[] args) {

        /*
            Type java code by using while loop,
            Write a program to print numbers from 1 to 5.
         */

        int i = 1;
        while(i<6){
            System.out.print(i + " ");
            i++;
        }


    }
}
