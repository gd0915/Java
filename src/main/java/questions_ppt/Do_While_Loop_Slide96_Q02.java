package questions_ppt;

public class Do_While_Loop_Slide96_Q02 {

    public static void main(String[] args) {

        /*
        Write a program to print numbers from 10 to 3 on the console by using do-while loop.
         */

        int i = 10;

        do {
            System.out.print( i + " ");
            i--;
        }while(i>2);








    }
}
