package questions_ppt;

public class Do_While_Loop_Slide96_Q04 {

    public static void main(String[] args) {

        //Write a program to print letters from c to m on the console by using do-while loop.

        char ch= 'c';
        String result = "";

        do {
            result = result + " " + ch;
            ch++;
        }while(ch <'n');
        System.out.print(result);

















    }
}
