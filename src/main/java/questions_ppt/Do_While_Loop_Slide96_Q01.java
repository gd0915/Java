package questions_ppt;

public class Do_While_Loop_Slide96_Q01 {
    public static void main(String[] args) {

        /*
        Write a program to print numbers from 1 to 5 on the console by using do-while loop.
         */

        int i = 1;

        do{
            System.out.print( i + " ");
            i++;
        }while(i<6);







    }
}
