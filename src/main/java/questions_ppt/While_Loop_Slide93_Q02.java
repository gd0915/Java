package questions_ppt;

public class While_Loop_Slide93_Q02 {

    public static void main(String[] args) {

        /*
        Type java code by using while loop.
        Write a program that types first 30 consecutive odd integers.
           */
        int i=1;

        while(i<61){
            if(i%2 != 0){
                System.out.print(i + " ");
            }
            i++;
        }





    }
}
